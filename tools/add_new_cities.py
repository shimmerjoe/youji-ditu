def gen_md(c: dict) -> str:
    prov, city, tagline = c["prov"], c["city"], c["tagline"]
    tier1 = c["tier1"]
    spots = c["spots"]
    foods = c["foods"]
    nature = [n for n in c["nature"] if not n.endswith("占位")]
    spot_names = [s[0] for s in spots]
    L = []
    a = L.append
    a(f"# {prov}{city}旅游攻略\n")
    a("## 先看结论")
    a(f"- 城市定位：{tagline}")
    a(f"- 第一梯队：{('、'.join(tier1))}")
    a("- 高频玩法：结合小红书、哔哩哔哩、抖音常见路线，优先选择地标景区、夜市街区、当地早餐和季节限定景观")
    a("- 适合安排：半天看城市核心或夜市，1 天打一条经典线，2-3 天再加远郊/周边")
    a(f"- 必吃优先级：{('、'.join(foods))}\n")
    a("## 快速决策索引")
    a(f"- 半天：{tier1[0]} 或 城区老街")
    a(f"- 1 天：{tier1[0]} + {tier1[1]} + 夜间吃喝")
    a("- 2 天：经典景区 + 博物馆/古镇 + 远郊自然")
    a(f"- 雨天：博物馆、展馆、商圈、老街、非遗体验和当地餐厅")
    a("- 亲子：博物馆、低强度景区和夜市小吃")
    a(f"- 摄影：清晨/傍晚去核心景区，夜间去城区街巷\n")
    a("## 全年月份速查")
    for m in range(12):
        a(f"- {m + 1} 月")
        a(f"  - 适合去：{spot_names[m % len(spot_names)]}")
        a(f"  - 吃什么：{foods[m % len(foods)]}")
        a(f"  - 注意什么：{MONTH_NOTE[m]}")
    a("")
    a("## 季节限定景观")
    a(f"- 春：{tier1[0]} 山花与踏青")
    a(f"- 夏：{nature[0] if nature else tier1[1]} 避暑戏水")
    a(f"- 秋：山林彩叶与丰收时令")
    a(f"- 冬：温泉、庙会或雪景（按当年公告）\n")
    a("## 核心景点")
    for name, desc in spots:
        a(f"- {name}：{desc}")
    a("")
    a("## 美食总表")
    for f in foods:
        a(f"- {f}：当地代表风味，认准老店和本地推荐")
    a("")
    a("## 餐厅和店铺")
    a("- 优先本地口碑老店与早市摊点，避开纯景区餐厅")
    a("- 饭点错峰，名店排队时段提前或延后")
    a("- 夜市与小吃街适合一次尝多样\n")
    a("## 自然、远郊和周边")
    for n in nature:
        a(f"- {n}：自然山水/远郊，建议留半天到一天")
    a("")
    a("## 路线模板")
    a(f"- 经典 1 日：{tier1[0]} → 城区午餐 → {tier1[1] if len(tier1) > 1 else '老街'} → 夜市")
    a(f"- 自然 2 日：第一天城市核心 + 博物馆，第二天 {nature[0] if nature else tier1[-1]} 远郊")
    a("- 周边延伸：可与同省邻近城市串线\n")
    a("## 避坑和取舍")
    a("- 山区景区注意天气与末班车/索道时间")
    a("- 网红打卡点错峰，门票与开放时间以官方为准")
    a("- 远郊景点交通班次少，自驾或包车更灵活\n")
    a("## 出发前复核清单")
    a("- 开放时间、门票、预约和天气出发前再次复核")
    a("- 住宿与热门项目旺季提前预订")
    a("- 准备好身份证、移动支付与常用药品\n")
    a("## 来源参考")
    a("- https://whc.unesco.org/")
    a(f"- https://www.mct.gov.cn/")
    return "\n".join(L) + "\n"


