def is_open_area(name: str) -> bool:
    keys = ("街", "路", "古城", "古镇", "夜市", "广场", "外滩", "海滨", "西湖", "八廓", "大巴扎", "环海")
    return any(key in name for key in keys)


def time_price(name: str) -> tuple[str, str]:
    if is_open_area(name):
        return "开放式区域，全天或以现场管理为准", "开放式/免费，以店铺、停车、游船或项目消费为准"
    if any(key in name for key in ("博物", "博物院", "纪念", "旧居", "故居")):
        return "常见周二至周日 09:00-17:00，周一闭馆或节假日调整", "多为免费预约，特展/讲解/临展以馆方公告为准"
    if any(key in name for key in ("寺", "宫", "祠", "庙", "院")):
        return "常见 08:00-17:30，宗教场所和节假日以公告为准", "门票、香花券或预约政策以官方/现场为准"
    return "常见 08:00-17:30，夜游、索道、游船和淡旺季以官方公告为准", "门票、观光车、索道、游船或套票以官方景区/购票平台为准"


