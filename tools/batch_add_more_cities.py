def parse_city(line: str) -> dict:
    p, c, slug, sub, top, food, streets, museums, nearby = line.split("|")
    return {
        "p": p,
        "c": c,
        "slug": slug,
        "sub": sub,
        "top": top.split(";"),
        "food": food.split(";"),
        "streets": streets.split(";"),
        "museums": museums.split(";"),
        "nearby": nearby.split(";"),
        "src": ["https://www.mct.gov.cn/", PROVINCE_SOURCES.get(p, "https://www.gov.cn/")],
    }


CITIES = [parse_city(line.strip()) for line in DATA.strip().splitlines() if line.strip()]


def update_record(cities: list[dict]) -> None:
    if not RECORD.exists():
        return
    text = RECORD.read_text(encoding="utf-8")
    lines = [
        "## 第三批新增城市索引（更多城市）",
        f"- 本轮继续新增 {len(cities)} 个城市/地区，补齐更多地级市、热门海滨、古城、山水、边境、草原、沙漠和区域中转目的地。",
        "- 每个城市均已生成独立 Markdown，并会自动进入网页页面列表、首页筛选、全站搜索和顶部导航。",
        "",
    ]
    current = None
    for city in cities:
        province, name = city["p"], city["c"]
        if province != current:
            current = province
            lines.append(f"### {province}")
        lines.extend(
            [
                f"- {name}",
                f"  - 第一梯队：{'、'.join(city['top'])}",
                f"  - 必吃美食：{'、'.join(city['food'])}",
                "  - 推荐路线：半天看城市核心或夜市，1 天打一条经典线，2-3 天再加远郊/周边。",
                "  - 适合季节：查看该城市“全年月份速查”和“季节限定景观”，按花期、丰水期、秋色、雪景、海滨、温泉或节庆取舍。",
                "  - 避坑信息：出发前复核门票、开放时间、预约、天气、交通和返程，不把多个远郊硬塞进同一天。",
            ]
        )
    section = "\n".join(lines) + "\n\n"
    pattern = r"\n## 第三批新增城市索引（更多城市）\n.*?(?=\n## )"
    if "## 第三批新增城市索引（更多城市）" in text:
        text = re.sub(pattern, "\n" + section.rstrip() + "\n", text, flags=re.S)
    else:
        text = text.replace("\n## 先看总览\n", "\n" + section + "## 先看总览\n", 1)
    summary = f"- 第三批已批量新增 {len(cities)} 个城市/地区：" + "、".join(city["c"] for city in cities) + "\n"
    if "第三批已批量新增" not in text:
        marker = "- 第二批已批量新增"
        idx = text.find(marker)
        if idx >= 0:
            end = text.find("\n", idx)
            text = text[: end + 1] + summary + text[end + 1 :]
    RECORD.write_text(text, encoding="utf-8")


def main() -> None:
    CITY_DIR.mkdir(parents=True, exist_ok=True)
    meta = {}
    if META_FILE.exists():
        try:
            meta = json.loads(META_FILE.read_text(encoding="utf-8"))
        except json.JSONDecodeError:
            meta = {}
    written = []
    skipped = []
    for city in CITIES:
        filename = f"{city['p']}_{city['c']}.md"
        if city["p"] in DIRECT and city["p"] == city["c"]:
            filename = f"{city['c']}.md"
        path = CITY_DIR / filename
        if path.exists():
            skipped.append(city)
        else:
            path.write_text(base.make_doc(city), encoding="utf-8")
            written.append(city)
        stem = path.stem
        title = f"{city['c']}旅游攻略" if city["p"] in DIRECT and city["p"] == city["c"] else f"{city['p']}{city['c']}旅游攻略"
        meta[stem] = {
            "slug": city["slug"],
            "title": title,
            "subtitle": city["sub"],
            "accent": "#5f6f73",
            "image_query": f"{city['c']} {city['p']} china travel",
        }
    META_FILE.write_text(json.dumps(meta, ensure_ascii=False, indent=2), encoding="utf-8")
    update_record(CITIES)
    print(f"cities={len(CITIES)} written={len(written)} skipped={len(skipped)}")
    for city in written:
        print(f"{city['p']}_{city['c']}")


if __name__ == "__main__":
    main()
